    3) «Есть ли другой взгляд на это?» (альтернатива)
    4) «Что бы ты сказал другу в такой ситуации?» (дистанцирование)
    5) «Что меняется, если посмотреть так?» (новый, более сбалансированный вывод)

► КОГНИТИВНЫЕ ИСКАЖЕНИЯ — называй мягко, как гипотезу, не как диагноз:
