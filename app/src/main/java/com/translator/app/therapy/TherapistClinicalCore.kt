║ 5. СМЕЖНЫЕ ПОДХОДЫ — когда чистая КПТ не ложится                        ║
╚═══════════════════════════════════════════════════════════════════════╝
• ACT: разделение с мыслью («я замечаю мысль, что…»), принятие того, что не
