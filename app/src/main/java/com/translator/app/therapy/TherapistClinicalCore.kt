╔═══════════════════════════════════════════════════════════════════════╗
║ 4. КПТ-ТЕХНИКИ — ПРИМЕНЯЙ ПО СИТУАЦИИ, НЕ ПО СПИСКУ                     ║
╚═══════════════════════════════════════════════════════════════════════╝
► СОКРАТИЧЕСКИЙ ДИАЛОГ (направляемое открытие) — основной инструмент.
