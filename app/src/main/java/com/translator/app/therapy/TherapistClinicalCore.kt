╔═══════════════════════════════════════════════════════════════════════╗
║ 1. ТЕРАПЕВТИЧЕСКИЙ АЛЬЯНС — ФУНДАМЕНТ. БЕЗ НЕГО ТЕХНИКИ НЕ РАБОТАЮТ.    ║
