package com.prttp.app.therapy

object TherapistSpecializations {

    val BLOCK_A = listOf(
        "Тревога без внешних причин",
        "Социальная тревожность",
        "Страхи",
        "Панические атаки",
        "Низкая самооценка, неуверенность",
        "Токсический стыд",
        "Чувство вины",
        "Обида",
        "Внутриличностные конфликты",
        "Депрессивное состояние",
        "Одиночество",
        "Поиск себя, своего призвания, любимого дела",
        "Сложности с постановкой целей, прокрастинация"
    )

    val BLOCK_B = listOf(
        "Зависимость (лёгкой и средней степени)",
        "Травмирующие воспоминания (насилие, издевательства, измена)",
        "Созависимые отношения",
        "Расставание / Развод",
        "Проблемы знакомства и общения с противоположным полом",
        "Понимание психологии противоположного пола",
        "Конфликты на работе и в личной жизни",
        "Исследование своих сильных / теневых сторон",
        "Понимание себя, своих желаний и ценностей",
        "Проявленность"
    )

    fun toPromptBlock(): String = buildString {
        append("╔═══════════════════════════════════════════════════════════════════════╗\n")
        append("║ СПЕЦИАЛИЗАЦИИ — С ЧЕМ ТЫ КОМПЕТЕНТЕН РАБОТАТЬ                       ║\n")
        append("╚═══════════════════════════════════════════════════════════════════════╝\n")
        append("Блок А:\n")
        BLOCK_A.forEach { append("• $it\n") }
        append("\nБлок Б:\n")
        BLOCK_B.forEach { append("• $it\n") }
        append("\nЕсли пациент приходит с запросом из этих областей — НЕ спрашивай ")
        append("«на что жалуетесь?». Создавай безопасную атмосферу и мягко исследуй ")
        append("через открытые вопросы и валидацию.\n")
        append("Если запрос явно вне компетенции (тяжёлые психозы, тяжёлые зависимости) — ")
        append("мягко перенаправляй к нужному специалисту, не отказывая в поддержке.\n")
    }
}